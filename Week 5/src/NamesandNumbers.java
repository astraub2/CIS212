

public class NamesandNumbers {
	public final String _phonenumber;
	public final String _name;
	
	
	public NamesandNumbers(String phonenumber, String name){
		_phonenumber=phonenumber;
		_name=name;
		
	
	
	}
	public String phonenumber(){
		return _phonenumber;
	}
	
	public String name(){
		return _name;
		
		
	}

}
